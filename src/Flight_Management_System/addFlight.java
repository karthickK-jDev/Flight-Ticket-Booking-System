    public void autoID(){
        try{
            Statement s = con.createStatement();
            ResultSet rs = s.executeQuery("select MAX(flightid) from flight");
            rs.next();
            rs.getString("MAX(flightid)");
            if(rs.getString("MAX(flightid)") == null){
                txtflightid.setText("FID001");
            }
            else{
                long id = Long.parseLong(rs.getString("MAX(flightid)").substring(3,rs.getString("MAX(flightid)").length()));
                id++;
                txtflightid.setText("FID"+String.format("%03d", id));
            }
        } catch (SQLException ex) {
            Logger.getLogger(addFlight.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    
    }
