    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String flightid = txtflightid.getText();
        String flightname = txtflightname.getText();
        String source = txtsource.getText();
        String departure = txtdeparture.getText();
        String departtime = txtdeparttime.getText();
        String arrivetime = txtarrivetime.getText();
        String firstclass = txtfirstclass.getText();
        String businessclass = txtbusinessclass.getText();
        String economyclass = txteconomyclass.getText();
        if(txtflightname.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Please enter Flight Name");
            txtflightname.requestFocus();
        }
        else if(txtsource.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Please enter Source");
            txtsource.requestFocus();
        }
        else if(txtdeparture.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Please enter Destination");
            txtdeparture.requestFocus();
        }
        else if(txtdeparttime.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Please enter Departure Timing");
            txtdeparttime.requestFocus();
        }
        else if(txtarrivetime.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Please enter Arrival Timing");
            txtarrivetime.requestFocus();
        }
        else if(txtfirstclass.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Please enter First Class Price");
            txtfirstclass.requestFocus();
        }
        else if(txtbusinessclass.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Please enter Business Class Price");
            txtbusinessclass.requestFocus();
        }
        else if(txteconomyclass.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Please enter Economy Class Price");
            txteconomyclass.requestFocus();
        }
        else{
            try{
                PreparedStatement ps = con.prepareStatement("insert into flight(flightid,flightname,source,departure,departtime,arrivetime,firstclass,businessclass,economyclass) values(?,?,?,?,?,?,?,?,?)");
                ps.setString(1,flightid);
                ps.setString(2,flightname);
                ps.setString(3,source);
                ps.setString(4,departure);
                ps.setString(5,departtime);
                ps.setString(6,arrivetime);
                ps.setString(7,firstclass);
                ps.setString(8,businessclass);
                ps.setString(9,economyclass);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null,"Date Added Successfully");
                autoID();
                clear();
                txtflightname.requestFocus();

            } catch (SQLException ex) {
                Logger.getLogger(addFlight.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

