    public void tableUpdate(){
        
        int a;
        try{
            PreparedStatement ps = con.prepareStatement("select * from ticket");
            ResultSet rs = ps.executeQuery();
            
            ResultSetMetaData rsmd = rs.getMetaData();
            a = rsmd.getColumnCount();
            DefaultTableModel dtm = (DefaultTableModel)jTable1.getModel();
            dtm.setRowCount(0);
            
            while(rs.next()){
                Vector v = new Vector();
                for(int i=1;i<=a;i++){
                    v.add(rs.getString("id"));
                    v.add(rs.getString("customerid"));
                    v.add(rs.getString("flightid"));
                    v.add(rs.getString("source"));
                    v.add(rs.getString("destination"));
                    v.add(rs.getString("class"));
                    v.add(rs.getString("price"));
                    v.add(rs.getString("seat"));
                    v.add(rs.getString("total"));
                }
                dtm.addRow(v);
            }
        } catch (SQLException ex) {
            Logger.getLogger(viewticket.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
